
public class Solution {

	/**
	 * Approach: Pretty self explanatory, the return is the addition of the two
	 * 			 integers.
	 * 
	 * Time complexity: O(1) --> Only one operation is performed no matter of input size.
	 * 
	 * Space complexity: O(1) --> Done in place.
	 * 
	 * 
	 * @param num1
	 * @param num2
	 * @return
	 */
	public int sum(int num1, int num2) {
		return num1 + num2;
	}
}
